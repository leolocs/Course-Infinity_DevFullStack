// Atividade pratica - 7: Solicite ao usuário que insira um valor em metros usando o prompt e, em seguida, converta esse valor para centímetros (1 metro = 100 centímetros) e
// exiba o resultado no console.

const metros = Number(prompt("Digite um valor em metros: "))
const centimetros = metros*100

alert(`O valor digitado convertido para centimetros é: ${centimetros}`)